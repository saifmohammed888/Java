package com.corejava;

public class Variables 							  		//class is a Keyword 
{
	public static void main(String args [])  				    
	{
		String Name = "Saif"; 	   				 		 //Name is a Variable with String as Data type
		System.out.println(Name);
		System.out.println("sum: "+(10+20));   			 //The addition is an operator
		System.out.println("product: "+10*20);  
		System.out.println("difference: "+(10-20));  
		System.out.println("division: "+10/20);  
		
		
		final int a = 10;								 //The integer a is made 	constant
		System.out.println(a);
		//a =20;									
		System.out.println(a);
	}
}
